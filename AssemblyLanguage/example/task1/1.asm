.MODEL SMALL
.STACK 100H
.DATA
    numbers DW 10 DUP(?)
    maxValue DW ?
    newline DB 0DH, 0AH, '$'

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV CX, 10
    MOV SI, 0

INPUT_LOOP:
    CALL READ_INT
    MOV numbers[SI], AX
    ADD SI, 2
    LOOP INPUT_LOOP

    CALL FIND_MAX

    MOV DX, OFFSET newline
    MOV AH, 09H
    INT 21H

    CALL PRINT_INT

    MOV AH, 4CH
    INT 21H
MAIN ENDP

READ_INT PROC
    MOV CX, 0
    MOV AX, 0
    MOV SI, 0
NEXT_DIGIT:
    MOV AH, 01H
    INT 21H
    CMP AL, 0DH
    JE END_INPUT
    SUB AL, '0'
    MOV DX, CX
    MOV CX, 10
    MUL CX
    ADD AX, DX
    JMP NEXT_DIGIT
END_INPUT:
    RET
READ_INT ENDP

FIND_MAX PROC
    MOV SI, 0
    MOV AX, numbers[SI]
    MOV CX, 9
CHECK_NEXT:
    ADD SI, 2
    CMP numbers[SI], AX
    JLE SKIP
    MOV AX, numbers[SI]
SKIP:
    LOOP CHECK_NEXT
    MOV maxValue, AX
    RET
FIND_MAX ENDP

PRINT_INT PROC
    MOV AX, maxValue
    MOV CX, 0
    MOV BX, 10
CONVERT_LOOP:
    MOV DX, 0
    DIV BX
    PUSH DX
    INC CX
    CMP AX, 0
    JNE CONVERT_LOOP
PRINT_LOOP:
    POP DX
    ADD DL, '0'
    MOV AH, 02H
    INT 21H
    LOOP PRINT_LOOP
    RET
PRINT_INT ENDP

END MAIN
